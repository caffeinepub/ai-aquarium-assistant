export function checkFishCompatibility(fishNames: string[]): {
  compatible: boolean;
  conflicts: Array<[string, string]>;
} {
  const conflicts: Array<[string, string]> = [];

  for (let i = 0; i < fishNames.length; i++) {
    for (let j = i + 1; j < fishNames.length; j++) {
      const nameA = fishNames[i];
      const nameB = fishNames[j];

      const fishA = fishDatabase.find(
        (f) => f.name.toLowerCase() === nameA.toLowerCase(),
      );
      const fishB = fishDatabase.find(
        (f) => f.name.toLowerCase() === nameB.toLowerCase(),
      );

      let conflict = false;

      if (fishA) {
        const incompat = fishA.incompatibleWith.some(
          (ic) =>
            ic.toLowerCase() === nameB.toLowerCase() ||
            nameB.toLowerCase().includes(ic.toLowerCase()) ||
            ic.toLowerCase().includes(nameB.toLowerCase()),
        );
        if (incompat) conflict = true;
      }

      if (fishB) {
        const incompat = fishB.incompatibleWith.some(
          (ic) =>
            ic.toLowerCase() === nameA.toLowerCase() ||
            nameA.toLowerCase().includes(ic.toLowerCase()) ||
            ic.toLowerCase().includes(nameA.toLowerCase()),
        );
        if (incompat) conflict = true;
      }

      if (conflict) {
        conflicts.push([nameA, nameB]);
      }
    }
  }

  return { compatible: conflicts.length === 0, conflicts };
}
